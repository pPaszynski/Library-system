            Przemyska Biblioteka Publiczna im. Ignacego Krasickiego jest samorządową instytucją kultury. Zaspokaja potrzeby czytelnicze, edukacyjne i kulturalne mieszkańców miasta, jak również wykonuje zadania w zakresie kultury na obszarze powiatu przemyskiego. Sieć biblioteczną PBP stanowi Biblioteka  przy ul. Grodzkiej 8 oraz siedem filii na terenie miasta.

            Statystyki informują, że Biblioteka w 2013 r. zarejestrowała 9 305 czytelników, a jej czytelnie odwiedziło 7 417 osób. Wypożyczono i udostępniono 183 478 woluminów. W 2014 r. w związku z przeprowadzką do nowego lokalu Biblioteka nie była czynna przez kilka miesięcy,  zarejestrowano 6 924 czytelników, czytelnie natomiast odwiedziło 4 768 osób, a udostepniono 132 675 wol.

            Od  blisko siedemdziesięciu lat Przemyska Biblioteka Publiczna wpisuje się w krajobraz życia kulturalnego miasta kierując do jego mieszkańców szeroką ofertę różnego rodzaju imprez, zarówno dla dzieci i młodzieży, jak i dorosłych oraz seniorów. Wśród licznych form pracy kulturalno – edukacyjnej znajdują się konkursy: literackie, językowe, multimedialne, plastyczne oraz turnieje, spotkania autorskie, odczyty, lekcje biblioteczne, dyskusje nad książką, koncerty.

